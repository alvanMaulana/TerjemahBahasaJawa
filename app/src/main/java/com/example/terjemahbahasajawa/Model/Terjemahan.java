package com.example.terjemahbahasajawa.Model;

public class Terjemahan {
    private int id;
    private String  kata;
    private String arti;


    private String indonesia;
    private String ngoko;
    private String krama;
    private String halus;




    public String getKrama() {
        return krama;
    }

    public void setKrama(String krama) {
        this.krama = krama;
    }





    public String getIndonesia() {
        return indonesia;
    }

    public void setIndonesia(String indonesia) {
        this.indonesia = indonesia;
    }

    public String getNgoko() {
        return ngoko;
    }

    public void setNgoko(String ngoko) {
        this.ngoko = ngoko;
    }

    public String getHalus() {
        return halus;
    }

    public void setHalus(String halus) {
        this.halus = halus;
    }



    public Terjemahan() {


    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getKata() {
        return kata;
    }

    public void setKata(String kata) {
        this.kata = kata;
    }

    public String getArti() {
        return arti;
    }

    public void setArti(String arti) {
        this.arti = arti;
    }



}
