package com.example.terjemahbahasajawa.Model;

public class Hasil {
    private int id;
    private String  kata;
    private String arti;

    public int getId() {

        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getKata() {
        return kata;
    }

    public void setKata(String kata) {
        this.kata = kata;
    }

    public String getArti() {
        return arti;
    }

    public void setArti(String arti) {
        this.arti = arti;
    }
}
